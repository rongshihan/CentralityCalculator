package Entity;

public class ClosenessE {
    String name;
    Double icc;
    Double occ;
    Double nicc;
    Double nocc;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getIcc() {
        return icc;
    }

    public void setIcc(Double icc) {
        this.icc = icc;
    }

    public Double getOcc() {
        return occ;
    }

    public void setOcc(Double occ) {
        this.occ = occ;
    }

    public Double getNicc() {
        return nicc;
    }

    public void setNicc(Double nicc) {
        this.nicc = nicc;
    }

    public Double getNocc() {
        return nocc;
    }

    public void setNocc(Double nocc) {
        this.nocc = nocc;
    }

}
