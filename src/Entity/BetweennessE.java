package Entity;

public class BetweennessE {
    String name;
    Double bc;//绝对中间中心度
    Double nbc;//相对中间中心度

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getBc() {
        return bc;
    }

    public void setBc(Double bc) {
        this.bc = bc;
    }

    public Double getNbc() {
        return nbc;
    }

    public void setNbc(Double nbc) {
        this.nbc = nbc;
    }
}
